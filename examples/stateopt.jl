## Example 8.1.2
n = 8
a = [[1;4;7], [1;4;8], [2;4;7], [2;4;8], [3;4;7], [3;4;8], [4;7], [4;8]]
ca = [1/2; 1/2; 1/2; 1/2; 1/2; 1/2; 1/2; 1/2]
b = [[1;5;7], [1;5;8], [1;6;7], [1;6;8], [2;5;7], [2;5;8], [2;6;7], [2;6;8], [3;5;7], [3;5;8], [5;7], [5;8]]
cb = [1/2; -1/2; 1/2; -1/2; -1/2; 1/2; 1/2; -1/2; 1/2; -1/2; -1/2; 1/2]
c = [[1], [2]]
cc = [1; 1]
supp = Vector{Vector{Vector{Int}}}(undef, 275)
coe = zeros(275)
k = 1
for i = 1:8, j = 1:12
    supp[k] = sort([a[i], b[j]])
    coe[k] = -2*ca[i]*cb[j]
    k += 1
end
for i = 1:8, j = 1:2
    supp[k] = sort([a[i], c[j]])
    coe[k] = -2*ca[i]*cc[j]
    k += 1
end
for i = 1:12, j = 1:2
    supp[k] = sort([b[i], c[j]])
    coe[k] = -2*cb[i]*cc[j]
    k += 1
end
for i = 1:8
    supp[k:k+1] = [[a[i]], [a[i], a[i]]]
    coe[k:k+1] = [-8*ca[i], ca[i]^2]
    k += 2
    for j = i+1:8
        supp[k] = [a[i], a[j]]
        coe[k] = 2*ca[i]*ca[j]
        k += 1
    end
end
for i = 1:12
    supp[k:k+1] = [[b[i]], [b[i], b[i]]]
    coe[k:k+1] = [-8*cb[i], cb[i]^2]
    k += 2
    for j = i+1:12
        supp[k] = [b[i], b[j]]
        coe[k] = 2*cb[i]*cb[j]
        k += 1
    end
end
for i = 1:2
    supp[k:k+1] = [[c[i]], [c[i], c[i]]]
    coe[k:k+1] = [8*cc[i], cc[i]^2]
    k += 2
    for j = i+1:2
        supp[k] = [c[i], c[j]]
        coe[k] = 2*cc[i]*cc[j]
        k += 1
    end   
end
d = 3
@time begin
opt,data = statepop([statepoly(supp, coe)], n, d, constraint="unipotent", vargroup=[3;3;2], TS="block", solve=true, bilocal=[3;7])
# opt,data = statepop(data, TS="block", bilocal=[3;7])
end
println(-opt-16)

